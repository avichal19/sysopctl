.SH DESCRIPTION
sysopctl Linux command to manage system resources effectively.
.SH HELP
sysopctl v0.1.0 is a command based system management tool to effectively manage various system operation in one run:
it performs operations like List Running Services, View System Load, Manage System Services, Checks Disk Usage, Monitor System Processes, Analyze System Logs and automates backups.
To run sysocptl command type following in terminal:
chmod +x sysocptl.sh
./sysocptl.sh
.SH OPTIONS
The sysopctl does not take any options.
